'''Escreva um programa que leia um valor em metros e o exiba convertido em milimetros'''

print(f"Esse programa converte o valor informado de metros para milímetros")
valor = int(input("Digite o valor para ser convertido em milímetros: "))

valor_mm = valor*1000

print(f"O valor informado convertido para milímetros é {valor_mm} mm")